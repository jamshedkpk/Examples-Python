""" 
Data type means a value that is stored inside a variable 
Python has 5 types of a data type
"""
# String data type that store text data
name="Jamshed Khan"
# Integer data type that store integer data
age=35
# Float data type that store decimal data
price =35.50
# Boolean data type that store true & false data
logedin =True
# None data type that store nothing but empty
none=None

print(type(name))
print(type(age))
print(type(price))
print(type(logedin))
print(type(none))