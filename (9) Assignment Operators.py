# Assignment operator is used to assign a value to a variable for this purpose = is used in python

num1=50
print(num1)
num1+=20 # num1=num1+20
print(num1)
num1-=20 # num1=num1-20
print(num1)
num1*=20 # num1=num1*20
print(num1)
num1/=5 # num1=num1/5
print(num1)
num1%=3 # num1=num1%3
print(num1)
num1**=5 # num1=num**5
print(num1)
num1//=5 # num1=num//5
print(num1)
